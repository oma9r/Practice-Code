class Tabular 
{
  public static void main(String [] args)
   { 
      System.out.println("N\t10*N\t100*N\t1000*N");
      System.out.println();

      for (int i=1; i<6;i++)
       {
         System.out.println(i + "\t" + i*10 + "\t" + i*100 + "\t" + i*1000);
       }//end of for
    }//end of main method
}//end of class      
      