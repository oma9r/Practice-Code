#include <iostream> 
using namespace std;

int main()
{
  for(int i=0;i<=27;i++)
  {
    if(i%3==0)
    {
        cout << i << endl;
    }
  }

  cout << "the end of FOR loop" << endl;

  int i=0;
  while(i<=27)
  {
    if(i%3==0)
    {
        cout << i << endl;
    }
    i++;
  }






    return 0;
}